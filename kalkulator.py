operator = input("masukan operator (+,-,/,*)")
angka1 = int(input("masukan bilangan pertama"))
angka2 = int(input("masukan bilangan ke dua"))
if operator == "+":{
    print(angka1+angka2)
}
if operator == "-":{
    print(angka1-angka2)
}
if operator == "/":{
    print(angka1/angka2)
}
if operator == "*":{
    print(angka1*angka2)
}


